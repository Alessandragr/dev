public class Dev_pessoa {
    private String nomeDev;
    private String sobrenomeDev;
    private String senioridadeDev;
    private int idadeDev;


    public void setNomeDev(String nomeDev){
        this.nomeDev = nomeDev;
    }

    public String getNomeDev(){
        return nomeDev;
    }


    public void setSobrenomeDev(String sobrenomeDev){
        this.sobrenomeDev = sobrenomeDev;
    }

    public String getSobrenomeDev(){
        return sobrenomeDev;
    }


    public void setSenioridadeDev(String senioridadeDev){
        this.senioridadeDev = senioridadeDev;
    }

    public String getSenioridadeDev(){
        return senioridadeDev;
    }


    public void setIdadeDev(int idadeDev){

        this.idadeDev = idadeDev;
    }

    public int getIdadeDev(){

        return idadeDev;
    }

}
